  private void btnAgregarActionPerformed(
      ActionEvent evt) { // GEN-FIRST:event_btnAgregarActionPerformed
    String respuesta = txtRespuesta.getText();

    boolean resultado =
        this.id == 0
            ? Respuesta.guardar(respuesta, fechaCreacion, idPregunta)
            : Respuesta.editar(this.idPregunta, fechaCreacion, respuesta);

    if (resultado) {
      JOptionPane.showMessageDialog(
          this,
          "El registro fue guardado correctamente",
          "REGISTRO GUARDADO",
          JOptionPane.INFORMATION_MESSAGE);
      dispose();
    } else {
      JOptionPane.showMessageDialog(
          this, "Ocurri√≥ un error al guardar el registro", "ERROR", JOptionPane.ERROR_MESSAGE);
      dispose();
    }
  } // GEN-LAST:event_btnAgregarActionPerformed

